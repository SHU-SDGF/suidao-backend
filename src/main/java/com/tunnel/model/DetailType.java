package com.tunnel.model;

import java.io.Serializable;
import javax.persistence.*;

import lombok.Getter;
import lombok.Setter;

/**
 * The persistent class for the detail_type database table.
 * 
 */
@Entity
@Table(name = "a_detail_type")
@NamedQuery(name = "DetailType.findAll", query = "SELECT d FROM DetailType d")
@Setter
@Getter
public class DetailType implements Serializable {
	private static final long serialVersionUID = 1L;

	@Id
	@Column(name = "DETAIL_TYPE", columnDefinition="nvarchar2")
	private String detailType;

}